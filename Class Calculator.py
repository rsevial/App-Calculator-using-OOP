# Creating a class named Calculator
class Calculator:
# Define function to add two inputted numbers
    def add(self, num1, num2):
        sum = num1 + num2
# return sum
        return sum
# Define function to subtract two inputted numbers
    def subtract(self, num1, num2):
        difference = num1 - num2
# return difference
        return difference
# Define function to multiply two inputted numbers
    def multiply(self, num1, num2):
        product = num1 * num2
# return product
        return product
# Define function to divide two inputted numbers
    def divide(self, num1, num2):
        quotient = num1 / num2
# return quotient
        return quotient